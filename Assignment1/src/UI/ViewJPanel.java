    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tbCarDetails.getModel();
        model.setRowCount(0);
         
        for (CarDetails cd : history.getHistory()){
             Object[] row = new Object[5];
             row[0]= cd;  
             row[1]= cd.getName();
             row[2]= cd.getModel();
             row[3]= cd.getDate();
             row[4]= cd.getServiceRecords();
             
             model.addRow(row);
            
              
        }
    }
