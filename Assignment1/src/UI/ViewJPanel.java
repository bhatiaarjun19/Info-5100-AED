    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        // TODO add your handling code here:
        int selectedRowIndex = tbCarDetails.getSelectedRow();

        if(selectedRowIndex< 0){

            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tbCarDetails.getModel();
        CarDetails selectedCars =(CarDetails) model.getValueAt(selectedRowIndex, 0);
        history.deleteDetails(selectedCars);
        JOptionPane.showMessageDialog(this, "Car Details Deleted");

        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


