    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
  
            
               int selectedRowIndex = tbCarDetails.getSelectedRow();
        
        if(selectedRowIndex< 0){
            
            JOptionPane.showMessageDialog(this, "Please enter details on Create page");
            return;
         }
        
        DefaultTableModel model = (DefaultTableModel) tbCarDetails.getModel();
       CarDetails selectedCars =(CarDetails) model.getValueAt(selectedRowIndex, 0);
       txtAddress.setText(String.valueOf(selectedCars.getAddress()));
       txtBrand.setText(String.valueOf(selectedCars.getBrand()));
       txtColor.setText(String.valueOf(selectedCars.getColor()));
       txtEngineNo.setText(String.valueOf(selectedCars.getEngineNo()));
       txtSSN.setText(String.valueOf(selectedCars.getSSN()));
       txtSeats.setText(String.valueOf(selectedCars.getSeats()));
       txtWarrantyRec.setText(String.valueOf(selectedCars.getWarrantyYears()));
       txtNumber.setText(String.valueOf(selectedCars.getPhoneNumber()));
       txtEmail.setText(String.valueOf(selectedCars.getEmail()));
       txtDL.setText(String.valueOf(selectedCars.getEmail()));
       jLabel1.setIcon(new ImageIcon(selectedCars.getPhoto())); 
      
       
    }//GEN-LAST:event_btnViewActionPerformed

